package com.crs4.roodin.moduletester;

/**
 * @author ICT/LBS Team - CRS4 Sardinia, Italy
 *
 */
public class Point {
	public int x;
	public int y;
	
	public Point(){}
	
	/**
	 * @param x
	 * @param y
	 */
	public Point(int x, int y){
		this.x = x;
		this.y = y;
	}
	
	/**
	 * @return
	 */
	public int getX() {
		return x;
	}
	
	/**
	 * @param x
	 */
	public void setX(int x) {
		this.x = x;
	}
	
	/**
	 * @return
	 */
	public int getY() {
		return y;
	}
	
	/**
	 * @param y
	 */
	public void setY(int y) {
		this.y = y;
	}
	
}
